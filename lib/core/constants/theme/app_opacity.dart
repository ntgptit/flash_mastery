/// Shared opacity tokens for consistent translucency levels across the app.
class AppOpacity {
  AppOpacity._();

  /// Very light transparency (10% opacity).
  static const double extraLow = 0.1;

  /// Light transparency (20% opacity).
  static const double low = 0.2;

  /// Medium-light transparency (30% opacity).
  static const double mediumLow = 0.3;

  /// Medium transparency (40% opacity).
  static const double medium = 0.4;

  /// Medium-high transparency (50% opacity).
  static const double mediumHigh = 0.5;

  /// High transparency (60% opacity).
  static const double high = 0.6;

  /// Very high transparency (70% opacity).
  static const double veryHigh = 0.7;

  /// Overlay-level transparency (80% opacity).
  static const double overlay = 0.8;
}
